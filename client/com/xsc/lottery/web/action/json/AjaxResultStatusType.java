package com.xsc.lottery.web.action.json;

public enum AjaxResultStatusType
{
    /** 操作成功 */
    _0000("操作成功"), 
    
    /** 操作失败 */
    _0001("操作失败"),
    
    /** 未登录 */
    _00000("未登录"),
    
    /** 异常 */
    _10000(""), 
    ;
    
    private String value;

    private AjaxResultStatusType(String val)
    {
        this.value = val;
    }

    public String getValue()
    {
        return value;
    }

}
